// FUNCIÓN INTELIGENTE DE BÚSQUEDA
export const getVideoForLocation = (scope) => {
    // FIX 1: Si pulsamos "Sintonizar GPS", scope es el string 'gps'
    // Devolvemos el video de CIUDAD por defecto (calle genérica)
    if (scope === 'gps') return VIDEO_MAP.default_city;

    if (!scope) return VIDEO_MAP.default_city;

    // Normalizamos texto
    const city = scope.city?.toLowerCase().trim().replace(/ /g, "_");
    const country = scope.country?.toLowerCase().trim().replace(/ /g, "_");

    // 1. Prioridad CIUDAD: Si escribes ciudad, manda la ciudad
    if (city) {
        // Limpiamos tildes (málaga -> malaga)
        const cleanCity = city.normalize("NFD").replace(/[\u0300-\u036f]/g, ""); 
        
        // Buscamos coincidencia exacta o limpia
        if (VIDEO_MAP[cleanCity]) return VIDEO_MAP[cleanCity];
        if (VIDEO_MAP[city]) return VIDEO_MAP[city];
        
        // Si escribe una ciudad que no tenemos (ej: "Villanueva"), 
        // devolvemos el video de ciudad genérica, NO el del país (para mantener la inmersión urbana)
        return VIDEO_MAP.default_city;
    }

    // 2. Prioridad PAÍS: Solo si NO hay ciudad escrita
    if (country) {
        const cleanCountry = country.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
        if (VIDEO_MAP[cleanCountry]) return VIDEO_MAP[cleanCountry];
        if (VIDEO_MAP[country]) return VIDEO_MAP[country];
    }

    // 3. Fallback total
    return VIDEO_MAP.default_country;
};